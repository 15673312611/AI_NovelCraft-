-- Bảng AI Generator để lưu trữ các loại generator và prompt tương ứng
CREATE TABLE IF NOT EXISTS ai_generator (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL COMMENT 'Tên generator',
    description VARCHAR(500) COMMENT 'Mô tả ngắn về generator',
    icon VARCHAR(50) COMMENT 'Icon class hoặc tên icon',
    prompt TEXT NOT NULL COMMENT 'Prompt template cho generator này',
    category VARCHAR(50) DEFAULT 'writing' COMMENT 'Danh mục: writing, planning, character, etc',
    sort_order INT DEFAULT 0 COMMENT 'Thứ tự hiển thị',
    status TINYINT DEFAULT 1 COMMENT '1: active, 0: inactive',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) COMMENT='Bảng quản lý các AI Generator';

